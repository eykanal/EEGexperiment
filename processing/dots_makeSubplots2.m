function dots_makeSubplots2( gAvg_high, gAvg_mid, gAvg_low )

% load gAvg_high;
% load gAvg_mid;
% load gAvg_low;

% use these vars to set values
times      = [-0.7 0];
channels  = [276 277 279 278;
             147 149 152 150;
             146 148 153 151];
increment = [1 2 2];

% leave this alone, converts time to indices
time = [find(gAvg_high.time==times(1)) find(gAvg_high.time==times(2))];

%
% CHANNEL PLOTS
%

for m=1:size(channels,1)
    figure();
    for n=1:4
        subplot(2,2,n);
        plot(...
            gAvg_high.time(time(1):time(2)), gAvg_high.avg(channels(m,n), time(1):time(2)), '-r', ...
            gAvg_mid.time (time(1):time(2)), gAvg_mid.avg(channels(m,n) , time(1):time(2)),  '-b', ...
            gAvg_low.time (time(1):time(2)), gAvg_low.avg(channels(m,n) , time(1):time(2)),  '-k');
        title(gAvg_high.label(channels(m,n)));
        axis([times(1) times(2) -1.25e-13 1.25e-13]);
        line(get(gca,'YLim'),[0 0],'Color','k');
        line([0 0],get(gca,'YLim'),'Color','k');
        legend('High coh','Mid','Low','Location','NorthWest');
    end
end


